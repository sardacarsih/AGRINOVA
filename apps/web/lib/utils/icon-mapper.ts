/**
 * Maps icon string names to Lucide React icon components
 * Used to convert icon strings from ROLE_NAVIGATION to actual icon components
 */
export function getIconComponent(iconName: string): React.ComponentType<any> {
    const IconComponent = iconMap[iconName];

    if (!IconComponent) {
        console.warn(`Icon "${iconName}" not found in lucide-react. Using default icon.`);
        return Circle; // Fallback icon
    }

    return IconComponent;
}

/**
 * Type-safe icon names
 */
export type IconName = keyof typeof iconMap;

export function getIcon(iconName: IconName): React.ComponentType<any> {
    return iconMap[iconName] || Circle;
}
